public class Monster {
    private int posX;
    private int pozY;

    public Monster(int posX, int pozY) {
        this.posX = posX;
        this.pozY = pozY;
    }

    public int getPosX() {
        return posX;
    }

    public int getPozY() {
        return pozY;
    }

    public void setPosX(int posX) {
        this.posX = posX;
    }

    public void setPozY(int pozY) {
        this.pozY = pozY;
    }

}
